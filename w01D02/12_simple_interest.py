"""Simple interest.

SI = P*T*R / 100
"""
print("-----Simple Interest calculation-----")
name = input("Enter your name:")
p = float(input("Enter Principle: "))
t = int(input("Enter Number of years: "))
r = float(input("Enter rate of interest: "))

si = p * t * r / 100

print(f"Simple Interest is: {si}")
print("-----------------------")

